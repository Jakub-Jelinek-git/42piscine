#include <unistd.h>

void    print_grid(int n, int **grid)
{
    int i;
    int j;
	char c;

    j = 0;
    while (j < n)
    {
        i = 0;
        while (i < n -1)
        {
			c = grid[j][i] + '0';
            write(1, &c, 1);
			write(1, " ", 1);
            i++;
        }
		c = grid[j][i] + '0';
		write(1, &c, 1);
        write(1, "\n", 1);
        j++;
    }
}
